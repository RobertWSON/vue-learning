    <h2>How is Olive Oil Produced ?</h2>

    <p>Olive oil production (making of it) involves several steps and here is a list of steps used in process below.</p>

    <ol>
        <li><b>Harvesting:</b> This usually involves shaking tree branches or using specialized tools like a machete 
               to knock olives off a tree. <br>Harvesting for olives is usually carried out from late Autumn to early 
               winter when they are at their ripest and ready for eating.
        </li><br>

        <li><b>Transportation:</b> This happens after harvesting, when olives are transported to a processing facility as 
               quickly as possible, but most importantly preventing spillage for maximum quantity.
        </li><br>
        
        <li><b>Cleaning:</b> Olives are washed carefully with soap and water to remove any dirt and leaves that may have 
               been collected during harvesting. 
        </li><br>
        
        <li><b>Grinding:</b> Cleaned olives are crushed into a paste. Traditionally this was done using stone or wooden 
               presses, but now modern facilities have a large granite or stainless steel mill often with mechanical crushers
               to break down the olives.
        </li><br>
        
        <li><b>Malaxation:</b> This is a process where Olive paste is mixed for a period of time to allow oil droplets to
               combine (stick together). Malaxation helps to seperate oil from solid parts of olive paste.
        </li><br>
        
        <li><b>Seperation:</b> With this process olive oil paste is spread into fiber disks or mats, which are stacked and 
               pressed to extract oil. Modern seperators with a centrifugal rotating spread of olive paste, can 
               alternatively be used to seperate oil from paste. 
        </li><br>
        
        <li><b>Clarification:</b> This is a process where oil from seperation process may need further cleaning 
               (clarification) to remove any remaining water, solids or impurities.
        </li><br>
        
        <li><b>Storage:</b> Finished Olive oil product is stored in stainless steel tanks or other containers to preserve
               it's quality until it is ready to be bottled or packaged.
        </li>
    </ol>

